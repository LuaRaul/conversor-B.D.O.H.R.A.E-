    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        boolean b1, b2;
        b1 = b2 = true;
        try {
            boolean s = buttonGroup1.getSelection().isSelected();
            System.out.println(s);
        } catch (Exception e) {
            b1 = false;
        }

        try {
            boolean s = buttonGroup2.getSelection().isSelected();
            System.out.println(s);
        } catch (Exception e) {
            b2 = false;
        }
        if (b1 == false && b2 == false) {
            jLabelMessagem.setText("Selecione o sistema de origem e o sistema de destino");
            return;
        }
        if (b1 == true && b2 == true) {
            jLabelMessagem.setText("Conversão feita com sucesso");
            jLabelMessagem.setForeground(Color.BLACK);
            jLabelMessagem.setBackground(Color.white);
            String resultado = Conversor.converter(numero, origem, destino);
            jLabelMessagem.setText(resultado);
            jLabelMessagem.setFont(new Font(jLabelMessagem.getFont().getFontName(),jLabelMessagem.getFont().getStyle(), 24));

            return;
        }
        if (!b1) {
            jLabelMessagem.setText("Selecione o sistema de origem ");
            return;
        }
        if (!b2) {
            jLabelMessagem.setText("Selecione o sistema de destino");
            return;
        }
    }//GEN-LAST:event_jButton2ActionPerformed

