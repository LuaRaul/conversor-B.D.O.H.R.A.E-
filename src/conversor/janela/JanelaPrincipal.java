    private void jTextFieldConversaoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldConversaoKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        if (numero.length() > 0 && evt.getKeyCode() == evt.VK_BACK_SPACE) {
            numero = numero.substring(0, numero.length() - 1);
            System.err.println("Raul");
        }
        boolean b = conversor.Conversor.verificaExistCaracter(c, caractrs);
        if (b) {
            numero += c;
            System.err.println(numero);
        } else {
            evt.consume();
        }
    }//GEN-LAST:event_jTextFieldConversaoKeyTyped

